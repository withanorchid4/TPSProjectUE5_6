FNePyReloadReinstancer::FNePyReloadReinstancer(UClass* InClassToReinstance, EBlueprintCompileReinstancerFlags Flags)
	: FBlueprintCompileReinstancer(nullptr /** 不走基类逻辑 */, Flags)
{
	check(InClassToReinstance);
	check(InClassToReinstance->ClassDefaultObject);
	ClassToReinstance = InClassToReinstance;
	OriginalCDO = ClassToReinstance->ClassDefaultObject;

	struct FTempRemoveGeneratedFuncNative
	{
		explicit FTempRemoveGeneratedFuncNative(UClass* InClass)
		{
			for (TFieldIterator<UFunction> FuncIt(InClass); FuncIt; ++FuncIt)
			{
				if (UNePyGeneratedFunction* Func = Cast<UNePyGeneratedFunction>(*FuncIt))
				{
					if (Func->HasAnyInternalFlags(EInternalObjectFlags_NePyGeneratedTypeGCSafe))
					{
						Func->ClearInternalFlags(EInternalObjectFlags_NePyGeneratedTypeGCSafe);
						ProcessedFuncs.Add(Func);
					}
				}
			}
		}

		~FTempRemoveGeneratedFuncNative()
		{
			for (UNePyGeneratedFunction* Func : ProcessedFuncs)
			{
				Func->SetInternalFlags(EInternalObjectFlags_NePyGeneratedTypeGCSafe);
			}
		}

		TArray<UNePyGeneratedFunction*> ProcessedFuncs;
	};

	{
		// 在Duplicate之前，先去除掉UNePyGeneratedFunction的Native/RootSet标记，防止引用泄露
		FTempRemoveGeneratedFuncNative TempRemoveGeneratedFuncNative(InClassToReinstance);
		
		const bool bAvoidCDODuplication = true;
		DuplicatedClass = FBlueprintCompileReinstancer::MoveCDOToNewClass(ClassToReinstance, TMap<UClass*, UClass*>(), bAvoidCDODuplication);
		DuplicatedClass->AssembleReferenceTokenStream(true);
	}

	TArray<UObject*> ObjectsToChange;
	const bool bIncludeDerivedClasses = false;
	GetObjectsOfClass(ClassToReinstance, ObjectsToChange, bIncludeDerivedClasses);
	for (UObject* ObjectToChange : ObjectsToChange)
	{
		// ObjectToChange->SetClass(DuplicatedClass);
		((*ObjectToChange).*GetPrivate(UObjectBaseSetClassAccessor()))(DuplicatedClass);
	}

	TArray<UClass*> ChildrenOfClass;
	GetDerivedClasses(ClassToReinstance, ChildrenOfClass);
	for (UClass* ChildClass : ChildrenOfClass)
	{
		if (ChildClass->GetSuperClass() == ClassToReinstance)
		{
			ChildClass->SetSuperStruct(DuplicatedClass);
		}

		ChildClass->Bind();
		ChildClass->StaticLink(true);
		ChildClass->AssembleReferenceTokenStream(true);
	}
}

